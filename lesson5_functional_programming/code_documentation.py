"""
Строки документации модуля должны, как правило, перечислять классы, исключения, функции (и любые другие объекты), 
которые экспортируются модулем, с краткими пояснениями (в одну строчку) каждого из них. 
(Эти строки, как правило, дают меньше деталей, чем первая строка документации к объекту). 
Строки документации пакета модулей (т.е. строка документации в __init__.py) также должны включать модули и подпакеты.


Строки документации функции или метода должны обобщить его поведение и документировать свои аргументы, 
возвращаемые значения, побочные эффекты, исключения, дополнительные аргументы, именованные аргументы, 
и ограничения на вызов функции.


Строки документации класса обобщают его поведение и перечисляют открытые методы и переменные экземпляра. 
Если класс предназначен для подклассов, и имеет дополнительный интерфейс для подклассов, 
этот интерфейс должен быть указан отдельно (в строке документации). 
Конструктор класса должен быть задокументирован в документации метода __init__. Отдельные методы должны иметь свои строки документации.
"""