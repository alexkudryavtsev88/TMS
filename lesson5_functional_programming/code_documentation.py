"""
Строки документации - строковые литералы, которые являются первым оператором в модуле, функции, классе
или определении метода. Такая строка документации становится специальным атрибутом __doc__ этого объекта.
"""

"""
Все модули должны, как правило, иметь строки документации, и все функции и классы, экспортируемые модулем также должны 
иметь строки документации. Публичные методы (в том числе __init__) также должны иметь строки документации. 
Пакет модулей может быть документирован в __init__.py.
"""

"""
1. Однострочники предназначены для действительно очевидных случаев и должны умещаться на одной строке. 
"""

def my_func(a: int):
    """Return the square from input arg."""
    return a ** 2

"""
Однострочная запись документации не должна быть "подписью" параметров функции / метода (которые могут быть получены с помощью интроспекции):
"""

def function(a, b):
    """function(a, b) -> list"""
    return [a, b]

